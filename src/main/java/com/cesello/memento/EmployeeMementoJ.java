package com.cesello.memento;

public class EmployeeMementoJ {

    private String name;
    private String phone;

    public EmployeeMementoJ(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }


}
